// Перемешать массив (Fisher-Yates)
function shuffleArray(arr) {
  for (let i = arr.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [arr[i], arr[j]] = [arr[j], arr[i]];
  }
  return arr;
}

// Получить вопрос с учётом сложности на основе метров
// meters < 500  → только лёгкие (d=1)
// meters < 1000 → лёгкие + средние (d≤2)
// meters ≥ 1000 → все, включая сложные (d≤3)
// Последние N вопросов — чтобы не повторяться подряд
const _recentQ = [];
const _RECENT_MAX = 8;

function getQuestion(index, meters) {
  const maxDiff = meters < 500 ? 1 : meters < 1000 ? 2 : 3;
  let pool = QUESTIONS.filter(q => (TOPIC_DIFF[q.topic] || 1) <= maxDiff);
  // Исключаем недавно показанные
  const filtered = pool.filter(q => !_recentQ.includes(q));
  if (filtered.length > 0) pool = filtered;
  const q = pool[~~(Math.random() * pool.length)];
  _recentQ.push(q);
  if (_recentQ.length > _RECENT_MAX) _recentQ.shift();
  const correctText = q.a[q.correct];
  const shuffled = shuffleArray([...q.a]);
  return {
    topic: q.topic,
    q: q.q,
    a: shuffled,
    correct: shuffled.indexOf(correctText),
  };
}
